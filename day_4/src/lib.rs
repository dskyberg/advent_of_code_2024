pub fn read_data(data: &str) -> Vec<Vec<u8>> {
    let mut result = Vec::new();
    for line in data.split('\n') {
        result.push(line.as_bytes().to_vec());
    }
    result
}

pub fn rev_row(row: &[u8]) -> Vec<u8> {
    row.iter().rev().cloned().collect::<Vec<u8>>()
}

// Reverse each row
pub fn reverse(matrix: &Vec<Vec<u8>>) -> Vec<Vec<u8>> {
    let mut result = Vec::new();
    for row in matrix {
        result.push(rev_row(row));
    }
    result
}

// Transpose [[1,2,3],[4,5,6],[7,8,9]] to [[1,4,7],[2,5,8],[3,6,9]]
pub fn transpose(matrix: &[Vec<u8>]) -> Vec<Vec<u8>> {
    let mut result = Vec::new();
    let cols = matrix[0].len();
    for x in 0..cols {
        let mut col = Vec::new();
        for row in matrix {
            col.push(row[x]);
        }
        result.push(col);
    }

    result
}

/// This assumes a square matrix, and flips diagonally
/// [[0.0, 0.1, 0.2, 0.3]
///  [1.0, 1.1, 1.2, 1.3]
///  [2.0, 2.1, 2.2, 2.3]
///  [3.0, 3.1, 3.2, 3.3]]
///
/// [[0.0, 1.1, 2.2, 3.3],
///  [1.0, 2.1, 3.2],
///  [2.0, 3.1],
///  [3.0],
///  [0.1, 1.2, 2.3],
///  [0.2, 1.3]
///  [0.3]]
pub fn diagonal(matrix: &[Vec<u8>]) -> Vec<Vec<u8>> {
    let mut result = Vec::new();
    let m = matrix.len();
    let n = matrix[0].len();

    for d in 0..n {
        let mut row = 0;
        let mut col = d;
        let mut array = Vec::new();
        while row < m {
            array.push(matrix[row][col]);
            row += 1;
            if col == 0 {
                break;
            }
            col -= 1;
        }
        result.push(array);
    }
    for d in 1..m {
        let mut row = d;
        let mut col = n - 1;
        let mut array = Vec::new();
        while row < m {
            array.push(matrix[row][col]);
            row += 1;
            if col == 0 {
                break;
            }
            col -= 1;
        }
        result.push(array);
    }
    result
}

